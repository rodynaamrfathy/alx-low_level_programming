int add(int a, int b);
int sub(int a, int b);
int mult(int a, int b);
int divison(int a, int b);
int mod(int a, int b);

/**
 * add - substract two numbers
 * @a: int
 * @b: int
 *
 * Return: a + b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract two numbers
 * @a: int
 * @b: int
 *
 * Return: a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mult - substract two numbers
 * @a: int
 * @b: int
 *
 * Return: a x b.
 */
int mult(int a, int b)
{
	return (a * b);
}

/**
 * divison - substract two numbers
 * @a: int
 * @b: int
 *
 * Return: a / b.
 */
int divison(int a, int b)
{
	return (a / b);
}

/**
 * mod - substract two numbers
 * @a: int
 * @b: int
 *
 * Return: a % b.
 */
int mod(int a, int b)
{
	return (a % b);
}
